<p>"Southport &amp; Eddy" is Mad Bread's debut album. Featuring 13 original songs. The album takes you on a sonic journey through space and time, modernity and tradition, war and peace, love and loss.</p>

<p>Acoustic and electric guitars blend seamlessly with the more ancient tones of the banjo and mandolin. Mad Bread is traditional and groundbreaking all at once.</p>

<p>You can download the album mp3s above, or listen on the soundcloud player.  Full-quality sound is available on the CD.</p>

