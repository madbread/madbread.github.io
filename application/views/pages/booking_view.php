 <h5 class="news_item">What to expect:</h5>

<p>
 Mad Bread concerts are exciting, high-energy shows that keep the audience engaged and having fun. The band has over 5 years of experience playing anywhere they'll let us, from some of Chicago's top venues (Double Door, Martyrs', Navy Pier) to a personal concert right in your backyard.
</p>

<p>
 The music is a unique mix of bluegrass, rock, blues, and folk music. We want to get the audience’s toes tapping, feet dancing, hands clapping, and voices singing. Mad Bread’s philosophy is that the audience should enjoy listening as much as the band enjoys performing.
</p>

<p>
 There’s something for everyone at a Mad Bread concert: upbeat bluegrass sure to get the people moving, blues-rock tunes featuring hot electric guitar and harmonica, tear-in-your beer sing-alongs, and always a few surprises.
</p>

<h5 class="news_item">A note on responsibility:</h5>

<p>
We love playing music and we want every show to be a good experience for everyone involved. You can always expect us to work with you in a straightforward and professional manner, and always fulfill our commitments.</p>
	    
