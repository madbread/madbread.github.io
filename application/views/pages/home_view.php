<p>is a string band for the 21st century, playing a unique mixture of bluegrass, rock, folk and blues. The band blends traditional instruments with modern songwriting, creating innovative, genre-bending music that defies classification. At a concert, you'll hear them move effortlessly from boot-stompin' bluegrass jams, to psychedelic folk ballads, to Chicago-style blues-rock with stops everywhere in between.
</p>
<p>
  The Chicago-based band was formed 2006, starting out as 3 roommates just pickin' in the living room. The band began gigging in early 2007 and has brought their unique sound to dozens of venues across the city of Chicago and the Midwest. When performing, the band's philosophy is simple: the audience should enjoy listening as much as the band enjoys playing.  It's all about having a good time.
</p>
<p>
  Mad Bread's second album "Tune Back In" was released in November 2010 and is currently available online for <a href="https://soundcloud.com/madbread/sets/tune-back-in-full-album" target="_blank">free at Soundcloud.com</a>.  
</p>
