	<span class="gtr_heading">Band Bio and History</span>
</h4>
<div id="main_body">
	<div id="main_content">
	
  <p>If there's one thing you need to know about the story of Mad Bread, it's this:  <strong>It's all about having a good time.</strong></p>
  
  <p>The Chicago-based band was formed 2006, starting out as 3 roommates picking and singing for their own pleasure. The band began gigging in early 2007 and has brought their unique sound to dozens of venues across the city, delighting audiences at every stop.  When performing, the band's philosophy is simple: the audience should enjoy listening as much as the band enjoys playing.</p>
  
  <p>The group's self-produced debut album, <em>Southport & Eddy</em>, was released in January of 2008.  The album showcases the striking depth and originality of the band's material.  Of the 13 tracks, no two sound the same.  In an age of formulaic, safe and predictable music-making, Mad Bread is a breath of fresh air.</p>
  
  <p>Recording for the second album is currently underway.  The album should be released in late 2009 / early 2010.</p>
  
